
public class Frame {
	private int startTime;
	private int endTime;
	private int pageNum;
	private int processID;
	public Frame(int startTime, int endTime, int pageNum, int processID) {
		super();
		this.startTime = startTime;
		this.endTime = endTime;
		this.pageNum = pageNum;
		this.processID = processID;
	}
}
